const { ethers } = require("hardhat");
const fs = require("fs");
const path = require("path");

// Deploys FlashBankRouterV3. Unlike v2.1, v3 bootstraps its initial token config in the constructor
// (there are no providers yet, so no timelock is needed), and there is NO single-signature setTokenConfig
// to fall back on. Every later change goes through propose → CONFIG_TIMELOCK → execute (dual signature).

const DEFAULT_FEE_BPS = Number(process.env.FLASHBANK_FEE_BPS || "2");
const DEFAULT_MAX_LOAN = process.env.FLASHBANK_MAX_LOAN || "1000";
const DEFAULT_MAX_BORROW_BPS = Number(process.env.FLASHBANK_MAX_BORROW_BPS || "5000"); // 50% of pool
const DEFAULT_OWNER_FEE_BPS = Number(process.env.FLASHBANK_OWNER_FEE_BPS || "200"); // 2% of the fee
const MAX_OWNER_FEE_BPS = 2000; // mirrors the contract cap (20% of the fee)

const WETH_ADDRESSES = {
	"1": "0xC02aaA39b223FE8D0A0e5C4F27eAD9083C756Cc2",
	"8453": "0x4200000000000000000000000000000000000006",
	"42161": "0x82aF49447D8a07e3bd95BD0d56f35241523fBab1",
	"11155111": "0xdd13E55209Fd76AfE204dBda4007C227904f0a81",
};

async function main() {
	const [deployer] = await ethers.getSigners();
	const network = await ethers.provider.getNetwork();
	const chainId = String(network.chainId);

	console.log("Deploying FlashBankRouterV3");
	console.log("  Network chainId:", chainId);
	console.log("  Deployer:", deployer.address);

	const isTestnet = ["11155111", "84532", "84531", "421614"].includes(chainId);
	const fallbackAdmin = process.env.ADMIN_ADDRESS;
	const testnetAdmin = process.env.TESTNET_ADMIN_ADDRESS;
	const adminAddress = isTestnet && ethers.isAddress(testnetAdmin) ? testnetAdmin : fallbackAdmin;

	if (!adminAddress || !ethers.isAddress(adminAddress)) {
		console.error("Error: Missing admin. Set ADMIN_ADDRESS (and TESTNET_ADMIN_ADDRESS for testnets) in .env");
		process.exit(1);
	}
	if (DEFAULT_OWNER_FEE_BPS > MAX_OWNER_FEE_BPS) {
		console.error(`Error: FLASHBANK_OWNER_FEE_BPS (${DEFAULT_OWNER_FEE_BPS}) exceeds the v3 cap of ${MAX_OWNER_FEE_BPS}`);
		process.exit(1);
	}

	const weth = process.env.FLASHBANK_LIQUIDITY_TOKEN || WETH_ADDRESSES[chainId];

	let tokens = [];
	let configs = [];
	if (weth && ethers.isAddress(weth)) {
		tokens = [weth];
		configs = [{
			enabled: true,
			supportsPermit: (process.env.FLASHBANK_SUPPORTS_PERMIT || "true") === "true",
			feeBps: DEFAULT_FEE_BPS,
			maxFlashLoan: ethers.parseEther(DEFAULT_MAX_LOAN),
			wrapper: weth,
			maxBorrowBps: DEFAULT_MAX_BORROW_BPS,
			ownerFeeBps: DEFAULT_OWNER_FEE_BPS,
		}];
		console.log("  Bootstrapping WETH config:", weth);
	} else {
		console.log("  No known WETH for this chain — deploying with no initial token (configure later via propose/execute).");
	}

	console.log("  Admin (dual-control):", adminAddress);

	const Router = await ethers.getContractFactory("FlashBankRouterV3");
	const router = await Router.deploy(adminAddress, tokens, configs);
	await router.waitForDeployment();
	const routerAddress = await router.getAddress();

	console.log("\nFlashBankRouterV3 deployed to:", routerAddress);
	console.log("ROUTER=" + routerAddress); // machine-readable for scripting/CI
	console.log("  VERSION:", await router.VERSION());
	console.log("  admin():", await router.admin());

	if (tokens.length > 0) {
		const cfg = await router.tokenConfigs(weth);
		console.log("\nWETH token configured:");
		console.log("  enabled:", cfg.enabled);
		console.log("  feeBps:", cfg.feeBps.toString(), `(${Number(cfg.feeBps) / 100}%)`);
		console.log("  maxFlashLoan:", DEFAULT_MAX_LOAN);
		console.log("  maxBorrowBps:", cfg.maxBorrowBps.toString(), `(${Number(cfg.maxBorrowBps) / 100}% of pool)`);
		console.log("  ownerFeeBps:", cfg.ownerFeeBps.toString(), `(${Number(cfg.ownerFeeBps) / 100}% of the fee)`);
	}

	// Persist constructor args so `hardhat verify` can reproduce them (struct arrays cannot be passed inline).
	const argsPath = path.join(__dirname, `verify-args-v3-${chainId}.js`);
	const argsModule = `// Auto-generated by deploy-router-v3.js for chainId ${chainId}.\n` +
		`module.exports = ${JSON.stringify([adminAddress, tokens, configs.map(serialiseConfig)], null, 2)};\n`;
	fs.writeFileSync(argsPath, argsModule);

	console.log("\nNext steps:");
	console.log(`  1. Verify on Etherscan:`);
	console.log(`     npx hardhat verify --network ${networkName(chainId)} --constructor-args ${path.relative(process.cwd(), argsPath)} ${routerAddress}`);
	console.log(`  2. Update website/.env.local: NEXT_PUBLIC_<CHAIN>_ROUTER_ADDRESS=${routerAddress}`);
	console.log(`  3. Run the integration check: npx hardhat run scripts/test-v3-integration.js --network ${networkName(chainId)}`);
}

function serialiseConfig(cfg) {
	// JSON cannot encode BigInt; store maxFlashLoan as a decimal string for the verify args module.
	return {
		enabled: cfg.enabled,
		supportsPermit: cfg.supportsPermit,
		feeBps: cfg.feeBps,
		maxFlashLoan: cfg.maxFlashLoan.toString(),
		wrapper: cfg.wrapper,
		maxBorrowBps: cfg.maxBorrowBps,
		ownerFeeBps: cfg.ownerFeeBps,
	};
}

function networkName(chainId) {
	return ({ "1": "ethereum", "8453": "base", "42161": "arbitrum", "11155111": "sepolia" })[chainId] || "sepolia";
}

main()
	.then(() => process.exit(0))
	.catch((error) => {
		console.error(error);
		process.exit(1);
	});
